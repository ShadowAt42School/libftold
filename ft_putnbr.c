#include "libft.h"

void	ft_putnbr(int n)
{
	char	interim;

	interim = 0;
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = n * (-1);
		}
		if (n >= 10)
		{
			ft_putnbr(n / 10);
		}
		interim = '0' + n % 10;
		write(1, &interim, 1);
	}
}
